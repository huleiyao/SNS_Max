@ActivityScope
public class MyCirclePresenter extends BasePresenter<MyCircleContract.Model, MyCircleContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public MyCirclePresenter(MyCircleContract.Model model, MyCircleContract.View rootView) {
        super(model, rootView);
    }

    /**
     * 获取我的圈子数据
     */
    @SuppressLint("CheckResult")
    public void getMyCircle(){
        mModel.getMyClicle()
                .subscribeOn(Schedulers.io())
                .observeOn(AndroidSchedulers.mainThread())
                .compose(RxLifecycleUtils.bindToLifecycle(mRootView))
                .subscribe(new ErrorHandleSubscriber<MyCircleDTO>(mErrorHandler){
                    @Override
                    public void onNext(MyCircleDTO myCircleDTO) {
                        mRootView.updateMyCircle(myCircleDTO);
                    }
                });
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }
}
